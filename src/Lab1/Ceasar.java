    private char mahoa(char c, int khoa) 
    {
        if (Character.isLetter(c))
        {
            c= (char)('A' + (Character.toUpperCase(c)-'A' +khoa)% 26);
        }
        return c;
    }

    private char giaima(char c, int khoa) {
        if (Character.isLetter(c))
        {
            c= (char)('A' + (Character.toUpperCase(c)-'A' - khoa)% 26);
        }
        return c;
    }
    
