    private void btnEncryptActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEncryptActionPerformed
        String plaintext = TxtArea_Plain.getText();
        String key = txtKey.getText();
        int khoa ;
        if(key.equals(" ") || plaintext.equals(""))
        {
            JOptionPane.showMessageDialog(null, "Vui lòng nhập thông tin!");
        }
        khoa = Integer.parseInt(key);
        if(khoa <0)
        {
            khoa = 26 - (-khoa % 26);   
        }
        String rs ="";
        for(int i =0; i< plaintext.length(); i++)
        {
            char c = mahoa(plaintext.charAt(i),khoa);
            rs = rs + c;
        }
        TxtArea_Cipher.setText(rs);   
    }//GEN-LAST:event_btnEncryptActionPerformed

